package chap_01;

public class _02_DataType {
    public static void main(String[] args) {
        System.out.println("안녕하세요?"); // String
        System.out.println(12); // int
        System.out.println(3.14); // long, double
        System.out.println(-12); // int
        System.out.println(true); // bool
        System.out.println(false); // bool

        System.out.println(123 + 234);
    }
}
